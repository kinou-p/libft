#include <libft.h>

int	ft_isalnum(int c)
{
	if (c <= 9 && c >= 0)
		return (c);
	else if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (c);
	else
		return (0);
}
